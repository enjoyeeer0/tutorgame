const initSqlJs = require('sql.js');
const fs = require('fs');
const path = require('path');

// Путь к файлу базы данных
const DB_PATH = path.join(__dirname, 'gameguides.db');

// Переменная для хранения объекта базы данных
let db;

// Сохраняет текущее состояние базы в файл
function save() {
  fs.writeFileSync(DB_PATH, Buffer.from(db.export()));
}

// Возвращает массив строк по SQL-запросу
function all(sql, params = []) {
  const statement = db.prepare(sql);
  statement.bind(params);

  const rows = [];
  while (statement.step()) {
    rows.push(statement.getAsObject());
  }

  statement.free();
  return rows;
}

// Возвращает одну строку или null
function get(sql, params = []) {
  return all(sql, params)[0] || null;
}

// Выполняет INSERT / UPDATE / DELETE, сохраняет файл, возвращает id новой записи
function run(sql, params = []) {
  db.run(sql, params);

  const result = db.exec('SELECT last_insert_rowid() as id');
  save();

  return result.length ? result[0].values[0][0] : null;
}

