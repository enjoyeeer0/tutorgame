// ===== ИНИЦИАЛИЗАЦИЯ БАЗЫ =====
async function init() {
  const SQL = await initSqlJs();

  // Загружаем существующую БД или создаём новую
  if (fs.existsSync(DB_PATH)) {
    db = new SQL.Database(fs.readFileSync(DB_PATH));
  } else {
    db = new SQL.Database();
  }

  // Создаём таблицы если их нет
  db.exec(`
    CREATE TABLE IF NOT EXISTS games (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      name TEXT NOT NULL,
      genre TEXT NOT NULL,
      image TEXT
    );

    CREATE TABLE IF NOT EXISTS categories (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      name TEXT NOT NULL
    );

    CREATE TABLE IF NOT EXISTS guides (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      title TEXT NOT NULL,
      content TEXT NOT NULL,
      author TEXT NOT NULL,
      game_id INTEGER NOT NULL,
      category_id INTEGER NOT NULL,
      created_at TEXT DEFAULT (datetime('now'))
    );

    CREATE TABLE IF NOT EXISTS comments (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      guide_id INTEGER NOT NULL,
      author TEXT NOT NULL,
      content TEXT NOT NULL,
      created_at TEXT DEFAULT (datetime('now'))
    );
  `);

  // Заполняем данными только если база пустая
  const gamesCount = get('SELECT COUNT(*) as count FROM games');
  if (!gamesCount || gamesCount.count === 0) {

    // Добавляем игры
    for (const game of GAMES) {
      db.run(
        'INSERT INTO games (name, genre, image) VALUES (?, ?, ?)',
        [game.name, game.genre, game.image]
      );
    }

    // Добавляем категории
    for (const categoryName of CATEGORIES) {
      db.run('INSERT INTO categories (name) VALUES (?)', [categoryName]);
    }

    // Добавляем гайды — находим id игры и категории по названию
    for (const guide of GUIDES) {
      const foundGame = get('SELECT id FROM games WHERE name = ?', [guide.game]);
      const foundCategory = get('SELECT id FROM categories WHERE name = ?', [guide.category]);

      if (foundGame && foundCategory) {
        db.run(
          'INSERT INTO guides (title, content, author, game_id, category_id) VALUES (?, ?, ?, ?, ?)',
          [guide.title, guide.content, guide.author, foundGame.id, foundCategory.id]
        );
      }
    }

    // Сохраняем всё в файл
    save();
    console.log(`База данных создана: ${GAMES.length} игр, ${GUIDES.length} гайдов`);
  }
}

